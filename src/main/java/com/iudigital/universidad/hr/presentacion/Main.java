    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        if (txtTipoID.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite el tipo de identificacion");
            txtTipoID.requestFocus();
            return;
        }

        if (txtNumeroID.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite el numero de identificacion");
            txtNumeroID.requestFocus();
            return;
        }

        if (txtNombres.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite los nombres");
            txtNombres.requestFocus();
            return;
        }

        if (txtApellidos.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite los apellidos");
            txtApellidos.requestFocus();
            return;
        }

        if (txtEstadoCivil.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite el estado civil");
            txtEstadoCivil.requestFocus();
            return;
        }

        if (txtSexo.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite el sexo");
            txtSexo.requestFocus();
            return;
        }

        if (txtDireccion.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite la direccion");
            txtDireccion.requestFocus();
            return;
        }

        if (txtNumeroTelefono.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite el numero de telefono");
            txtNumeroTelefono.requestFocus();
            return;
        }

        if (txtFechaNacimiento.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite la fecha de nacimiento");
            txtFechaNacimiento.requestFocus();
            return;
        }

        try {
            Funcionario funcionario = new Funcionario();
            String tipoIdentificacionIdStr = String.valueOf(txtTipoID.getText().trim());
            funcionario.setTipoIdentificacionId(Integer.parseInt(tipoIdentificacionIdStr));
            funcionario.setNumeroIdentificacion(txtNumeroID.getText().trim());
            funcionario.setNombres(txtNombres.getText().trim());
            funcionario.setApellidos(txtApellidos.getText().trim());
            String estadoCivilIdStr = String.valueOf(txtEstadoCivil.getText().trim());
            funcionario.setEstadoCivilId(Integer.parseInt(estadoCivilIdStr));
            funcionario.setSexo(txtSexo.getText().trim());
            funcionario.setDireccion(txtDireccion.getText().trim());
            funcionario.setNumeroTelefono(txtNumeroTelefono.getText().trim());
            funcionario.setFechaNacimiento(txtFechaNacimiento.getText().trim());

            funFuncionarioController.crear(funcionario);

            txtTipoID.setText("");
            txtNumeroID.setText("");
            txtNombres.setText("");
            txtApellidos.setText("");
            txtEstadoCivil.setText("");
            txtSexo.setText("");
            txtDireccion.setText("");
            txtNumeroTelefono.setText("");
            txtFechaNacimiento.setText("");
            
            listFuncionarios();
            
            JOptionPane.showMessageDialog(null, "Funcionario creado con exito");

        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "No se pudo crear el funcionario");
        }
    }//GEN-LAST:event_btnCrearActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        if (txtFuncionarioID.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Seleccione un funcionario de la lista");
            txtFuncionarioID.requestFocus();
            return;
        }

        int opt = JOptionPane.showConfirmDialog(null, "Esta seguro que desea eliminar el funcionario",
                "Confirmar salida", JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE);

        if (opt == 0) {
            try {
                funFuncionarioController.eliminarFuncionario(Integer.parseInt(txtFuncionarioID.getText()));
                txtFuncionarioID.setText("");
                txtTipoIDEdit.setText("");
                txtNumeroIDEdit.setText("");
                txtNombresEdit.setText("");
                txtApellidosEdit.setText("");
                txtEstadoCivilEdit.setText("");
                txtSexoEdit.setText("");
                txtDireccionEdit.setText("");
                txtNumeroTelefonoEdit.setText("");
                txtFechaNacimientoEdit.setText("");
                listFuncionarios();
                JOptionPane.showMessageDialog(null, "Funcionario eliminado con exito");
            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "No se pudo eliminar el funcionario");
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

